// One-off: freezes the authored HSK4-6 dialogue openings into base-dialogues-46.mjs so the
// lesson specs can extend them without re-typing, and so re-running the builder stays
// idempotent (the base is a file, never re-read from data/).
// Usage: node server/scripts/content/snapshot-dialogues-46.mjs > .../base-dialogues-46.mjs
import { readFile, readdir } from 'node:fs/promises';
import { DATA } from './lib.mjs';

const q = (value) => `'${String(value).replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
const ids = (await readdir(`${DATA}/lessons`)).filter((id) => /^hsk[456]-/.test(id)).sort();

const out = [
  '// Frozen snapshot of the original HSK4-6 dialogue openings (6 lines per lesson).',
  '// The lesson specs in hsk4-*.mjs / hsk5-*.mjs / hsk6-*.mjs continue these lines rather',
  '// than replacing them: the openings were already topic-specific, unlike the passages,',
  '// grammar and exercises they shipped with. Generated by snapshot-dialogues-46.mjs.',
  '// Line format: [speaker, simplified, pinyin, english, vietnamese]',
  '',
  'export default {'
];

for (const id of ids) {
  const [dialogue] = JSON.parse(await readFile(`${DATA}/dialogues/${id}/en.json`, 'utf8'));
  out.push(`  ${q(id)}: [`);
  for (const line of dialogue.lines) {
    out.push(`    [${q(line.speaker)}, ${q(line.simplified)}, ${q(line.pinyin)},`);
    out.push(`      ${q(line.english)},`);
    out.push(`      ${q(line.vi)}],`);
  }
  out.push('  ],');
}
out.push('};', '');
console.log(out.join('\n'));
